package test01;

public class Main {

    public static void main(String[] args) {
        //変数の宣言
        int x;
        int y;
        
        //x、という値の変数に2という数字を入れる。
        x = 2;
        
        //y、という値の変数に３という数字を入れる。
        y =3;
        
        //xとyの足し算が実行され、合計が出力される。
        System.out.println(x + y);
        
    }

}
